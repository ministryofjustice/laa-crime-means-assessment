CREATE TABLE IF NOT EXISTS crime_means_assessment.ass_criteria_child_weightings
(
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    ass_criteria_id BIGINT NOT NULL,
    lower_age_range DECIMAL(2,0) NOT NULL,
    upper_age_range DECIMAL(2,0) NOT NULL,
    weighting_factor DECIMAL(4,2) NOT NULL,
    date_created TIMESTAMP NOT NULL,
    user_created VARCHAR(10) NOT NULL,
    date_modified TIMESTAMP,
    user_modified VARCHAR(10),

    CONSTRAINT pk_ass_criteria_child_weightings PRIMARY KEY (ID),
    CONSTRAINT assessment_criteria_fk FOREIGN KEY (ass_criteria_id)
        REFERENCES crime_means_assessment.assessment_criteria (id)
);